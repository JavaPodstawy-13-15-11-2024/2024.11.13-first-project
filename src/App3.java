public class App3 {
    public static void main(String[] args) {
        int i = 0;
        System.out.println(i);
        System.out.println(i++); // i = 1
        System.out.println(++i); // i = 2
        System.out.println(i--); // i = 1
        System.out.println(--i); // i = 0
        System.out.println(i);

        int x = 5;
        x += 3;

        System.out.println(x);
    }
}
